import yaml  # Used to read YAML file


def extract_field_names_from_rule_tags(yaml_file_path: str) -> dict:
    """
    Read YAML file and return mapping of ruleTag -> fieldName
    """

    # Open the YAML file in read mode
    with open(yaml_file_path, "r") as file_pointer:
        yaml_data = yaml.safe_load(file_pointer)

    # Get the list of rules from YAML
    rules_list = yaml_data.get("rules", [])

    # Store final output here
    rule_tag_to_field_name = {}

    # Loop through each rule
    for single_rule in rules_list:
        # Read fieldName from current rule
        field_name = single_rule.get("fieldName")

        # Read ruleTags list from current rule
        rule_tags = single_rule.get("ruleTags", [])

        # If ruleTags is empty, skip that rule
        if not rule_tags:
            continue

        # Map each ruleTag to the fieldName
        for single_tag in rule_tags:
            rule_tag_to_field_name[single_tag] = field_name

    return rule_tag_to_field_name


